"""
Complexity:
- Time  : O(9^k) k = empty cells
- Space : O(1)
"""
from typing import List
from collections import defaultdict


class Solution:
    # Runtime: 83ms
    # Memory Usage: 19.64MB
    def solveSudoku(self, board: List[List[str]]) -> None:
        """
        Do not return anything, modify board in-place instead.
        """
        row = defaultdict(set)
        col = defaultdict(set)
        grid = defaultdict(set)

        for i in range(9):
            for j in range(9):
                if board[i][j] != ".":
                    val = board[i][j]
                    row[i].add(val)
                    col[j].add(val)
                    grid[(i//3, j//3)].add(val)

        def backtrack() -> bool:
            min_candidates = 10
            target = None

            for i in range(9):
                for j in range(9):
                    if board[i][j] == ".":
                        candidates = []
                        for d in "123456789":
                            if (
                                d not in row[i]
                                and d not in col[j]
                                and d not in grid[(i//3, j//3)]
                            ):
                                candidates.append(d)

                        if len(candidates) < min_candidates:
                            min_candidates = len(candidates)
                            target = (i, j, candidates)

                        if min_candidates == 1:
                            break
                if min_candidates == 1:
                    break

            if not target:
                return True

            i, j, candidates = target

            for d in candidates:
                board[i][j] = d
                row[i].add(d)
                col[j].add(d)
                grid[(i//3, j//3)].add(d)

                if backtrack():
                    return True

                board[i][j] = "."
                row[i].remove(d)
                col[j].remove(d)
                grid[(i//3, j//3)].remove(d)

            return False

        backtrack()


    # Best memory complexity solution (17MB)
    def solveSudokuBestMemory(self, board: List[List[str]]) -> None:
        """
        Do not return anything, modify board in-place instead.
        """
        rows = [set() for _ in range(9)]
        cols = [set() for _ in range(9)]
        boxes = [set() for _ in range(9)]
        empties = []
        for r in range(9):
            for c in range(9):
                val = board[r][c]
                if val == '.':
                    empties.append((r, c))
                else:
                    rows[r].add(val)
                    cols[c].add(val)
                    box_index = (r // 3) * 3 + (c // 3)
                    boxes[box_index].add(val)
        def backtrack(index):
            if index == len(empties):
                return True
            r, c = empties[index]
            box_index = (r // 3) * 3 + (c // 3)
            for ch in map(str, range(1, 10)):
                if (ch not in rows[r] and ch not in cols[c]
                and ch not in boxes[box_index]):
                    board[r][c] = ch
                    rows[r].add(ch)
                    cols[c].add(ch)
                    boxes[box_index].add(ch)
                    if backtrack(index + 1):
                        return True
                    board[r][c] = '.'
                    rows[r].remove(ch)
                    cols[c].remove(ch)
                    boxes[box_index].remove(ch)
            return False
        backtrack(0)


